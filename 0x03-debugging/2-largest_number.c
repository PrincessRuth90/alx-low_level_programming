#include "main.h"

/**
 * largest_number - returns the largest of 3 numbers
 * @a: first integar
 * @b: second integar
 * @c: third integar
 */ 

int main(a, b, c);
{
int largest;

if (a > b && a > c)
{
largest = a;
}
else if (b > c && b > a)
{
largest = b;
}
else if (c > b)
{
largest = c;
}
else
{
largest = b;
}
printf("%d is the largest number\n", largest);

return (largest);
}
